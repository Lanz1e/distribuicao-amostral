    private void txtPopulacaoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPopulacaoKeyTyped
        char c = evt.getKeyChar();
        
        if((c == ',')){
            evt.setKeyChar('.');
        }
        else if (!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE) || (c == '.'))) {
            evt.consume();
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Esse campo aceita somente números!","INVÁLIDO",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_txtPopulacaoKeyTyped

