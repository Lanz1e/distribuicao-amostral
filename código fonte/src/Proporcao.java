    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        if(txtErro.getText().equals("")||(txtPorcentagem.getText().equals("")&&txtPorcentagem.getText().equals("0"))){
            JOptionPane.showMessageDialog(null, "Certifique de preencher os campos!","AVISO",JOptionPane.WARNING_MESSAGE);
        }
        else{
            if(txtPopulacao.getText().equals("")||txtPopulacao.getText().equals("0"))
                popu=0.0;
            else
                popu=Double.parseDouble(txtPopulacao.getText());
            
            erro=Double.parseDouble(txtErro.getText());
            erro=erro/100.0;            
            porcent=Double.parseDouble(txtPorcentagem.getText());
            porcent=porcent/100.0;

            if(popu==0.0){
                //infinita
                try{
                    result=(nivelC*nivelC*porcent*(1-porcent))/(erro*erro);
                    lblResult.setText(result+" = "+Math.ceil(result));
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Ocorreu algum erro!","AVISO",JOptionPane.ERROR_MESSAGE);
                }
            }
            else{
                //finita
                //System.out.print("popu "+popu+"\nnivelC "+nivelC+"\nporcent "+porcent+"\nerro "+erro);
                try{
                    result=(popu*nivelC*nivelC*porcent*(1-porcent))/((popu-1)*erro*erro+nivelC*nivelC*porcent*(1-porcent));
                    lblResult.setText(result+" = "+Math.ceil(result));
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Ocorreu algum erro!","AVISO",JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

